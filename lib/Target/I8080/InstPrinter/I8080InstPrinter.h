#ifndef LLVM_LIB_TARGET_I8080_INSTPRINTER_I8080INSTPRINTER_H
#define LLVM_LIB_TARGET_I8080_INSTPRINTER_I8080INSTPRINTER_H

#include "I8080InstPrinterBase.h"

namespace llvm {
class I8080InstPrinter final : public I8080InstPrinterBase {
public:
  I8080InstPrinter(const MCAsmInfo &MAI, const MCInstrInfo &MII,
                 const MCRegisterInfo &MRI)
    : I8080InstPrinterBase(MAI, MII, MRI) {}

  // Autogenerated by tblgen.
  void printInstruction(const MCInst *MI, raw_ostream &OS) override;
  StringRef getRegName(unsigned RegNo) const override {
    return getRegisterName(RegNo);
  }
  static const char *getRegisterName(unsigned RegNo);
};
}

#endif
